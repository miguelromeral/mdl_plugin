/**
 * The renderer class to show single content if is needed in a page.
 *
 * @package    mod_league
 * @since      Moodle 3.0
 * @copyright  2018 Miguel Romeral
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class single_content_view implements \renderable {
 
    /** @var string Title (if exists). */
    public $title = null;
    
    /** @var string Content. */
    public $content = null;
    
    /**
     * Class constructor.
     * 
     * @param string $content Content.
     * @param string $title Title (if exists). 
     */
    public function __construct($content, $title = null) {
        $this->title = $title;
        $this->content = $content;
    }
}